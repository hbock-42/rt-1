#include <string.h>

char	*ft_strchr(const char *s, int c)
{
	if (!s)
		return (NULL);
	while (*s)
	{
		if (*s == (char)c)
			return ((char*)s);
		s++;
	}
	return ((!c) ? (char*)s : NULL);
}
